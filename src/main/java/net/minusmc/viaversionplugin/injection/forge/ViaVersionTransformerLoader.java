package net.minusmc.viaversionplugin.injection.forge;

import net.minecraftforge.fml.relauncher.IFMLLoadingPlugin;
import net.minusmc.viaversionplugin.injection.transformers.ViaVersionTransformer;
import org.spongepowered.asm.launch.MixinBootstrap;
import org.spongepowered.asm.mixin.MixinEnvironment;
import org.spongepowered.asm.mixin.Mixins;

import java.util.Map;

public class ViaVersionTransformerLoader implements IFMLLoadingPlugin {

    public ViaVersionTransformerLoader() {
        MixinBootstrap.init();
        Mixins.addConfiguration("viaforge.forge.mixin.json");
        MixinEnvironment.getDefaultEnvironment().setSide(MixinEnvironment.Side.CLIENT);
    }

    @Override
    public String[] getASMTransformerClass() {
        return new String[] {ViaVersionTransformer.class.getName()};
    }

    @Override
    public String getModContainerClass() {
        return null;
    }

    @Override
    public String getSetupClass() {
        return null;
    }

    @Override
    public void injectData(Map<String, Object> data) { }

    @Override
    public String getAccessTransformerClass() {
        return null;
    }
}
